#include <stdio.h>
#include "main.h"

/**
 * times_table - print the 9 times table
 *
 * Return: Always 0
 */

void times_table(void)
{
	int x;
	int y;

	for (x = '0'; x <= '9'; x++)
	{
	for (y = '0'; y <= '9'; y++)
	{
	if (x < y)
	_putchar(0 * 'y');
	_putchar(0 * 'x');
	}
	}

	_putchar('\n');





}
